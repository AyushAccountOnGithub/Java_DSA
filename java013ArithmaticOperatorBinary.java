public class java013ArithmaticOperatorBinary {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;

        System.out.println("a is : " +a);
        System.out.println("b is : " +b);

        System.out.println("Addition is : "+(a+b)); // 15
        System.out.println("Subtraction is : "+(a-b)); // 05
        System.out.println("Multiplication is : "+(a*b)); // 50
        System.out.println("Division is : "+(a/b)); // 2
        System.out.println("Modulo is : "+(a%b)); // 0
    }
}
